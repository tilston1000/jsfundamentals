// Relational or comparison operators

// > - more than
// < - less than
// >= - more than equal
// <= - less than equal

var result = 4 >= 5
console.log(result)

// equality operators
var x = 1
console.log(x == '1') // loose comparison(no validation on data type)
console.log(x === '1') // string comparison(validation on data type)